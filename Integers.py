age = 30
score = -85
high_score = 1000000 # Large integers are also supported

# Zero is also an integer
count = 0

#Performing Operations with Integers: Python supports various arithmetic operations on integers:
# Addition
total = age + score
print(total) # Output: -55

#Subtraction
difference = high_score - score
print(difference) # Output: 1000085

# Multiplication
product = age * count
print(product) # Output: 0